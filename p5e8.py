print('dime la altura del triangulo')
altura = int(input())
anchura = 0
for i in range((altura*2)-1):
    print('')
    if ((i)<(altura)):
        anchura = anchura+1
    if i>=(altura):
        anchura = anchura-1
    for j in range(anchura):
        print('*', end='')
        

    
    
  
    
    
